public class Journal  {
    public String name;
    public int volume;
    public int year;

    public String getName() {
        return name;
    }

    public int getVolume() {
        return volume;
    }

    public int getYear() {
        return year;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }

    public void setYear(int year) {
        this.year = year;
    }
    
    
    
}
